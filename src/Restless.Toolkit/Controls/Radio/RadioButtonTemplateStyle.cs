namespace Restless.Toolkit.Controls
{
    /// <summary>
    /// Provides an enumeration of values that may be used with <see cref="RadioButton.TemplateStyle"/>
    /// </summary>
    public enum RadioButtonTemplateStyle
    {
        /// <summary>
        /// Button style
        /// </summary>
        Button,
        /// <summary>
        /// Underline style
        /// </summary>
        Underline,
        /// <summary>
        /// The default style
        /// </summary>
        Default = Button,
    }
}